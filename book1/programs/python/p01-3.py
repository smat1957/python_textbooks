# iは、整数(Integer)型変数
i = 10
print( type( i ), '\t', i )

# aは、浮動小数点(float)型変数
a = 3.14159265
print( type( a ), '\t', a )

# xも、浮動小数点型変数
x = 271828e-5
print( type( x ), '\t', x )

# sは、文字列(str)型変数
s = 'I am a student.'
print( type( s ), '\t', s )

# bは、論理(bool)型変数
b = 10<30
print( type( b ), '\t', b )

# cは、複素数(complex)型変数
# jは、虚数単位
c = 2 + 1j
print( type( c ), '\t', c )
